<node TEXT="Service Catalogue Management" ID="ID_648286334" CREATED="1292817555031" MODIFIED="1399495212093"><richcontent TYPE="NOTE">

<html xmlns="http://www.w3.org/1999/xhtml">
<p>
<font face="Verdana">
<span>Purpose: To provide a single source of consistent 
information on all of the agreed services, and ensure 
it's widely available to those who are approved to 
access it.</span>
</font>
</p>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>Goal: To ensure that a Service Catalog is produced and 
maintained, containing accurate info on all operational 
services and those preparing to be run operationally.</span>
</font>
</p>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>Objective: to manage the information contained within 
the Service Catalog and to ensure that it is accurate 
and reflects the current details, status, interfaces and 
dependancies of all services that are being run, or are 
in preparation to run in the live environment.</span>
</font>
</p>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>4.1.2 Scope</span>
</font>
</p>
<p>
<font face="Verdana">
<span>To provide and maintain acurate info on all services in 
transition to production.</span>
</font>
</p>
<p>
<font face="Verdana">
<span>Activities should include:</span>
</font>
</p>
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Service definition</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Production and maintenance of the accurate SC</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interfaces, dependancies and consistency 
between the Srv. Catalogue and S. Portfolio</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interfaces and dependancies between services 
and supporting services from SC and CMS</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interfaces and dependancies between all 
services, and supporting components and CIs 
from SC and CMS</span>
</font>
</li>
</ul>
</ul>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>4.1.3 Value to the Business</span>
</font>
</p>
<p>
<font face="Verdana">
<span>SC is a single source of consistent information on all of 
the provided IT services. All areas of business can view 
an accurate, consistent picture of IT services, their 
details and status. It contains a Customer View of 
services, how they should be used, business proces 
they enable and QOS that the Customer can expect 
dor each of them.</span>
</font>
</p>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>4.1.4 Policies, principles and basic concepts</span>
</font>
</p>
<p>
<font face="Verdana">
<span>Service Portfolio should include all the future 
requirements for services. </span>
</font>
</p>
<p>
<font face="Verdana">
<span>Service Catalogue should include all pending and 
current services, with the summary of their 
characteristics, and details of the customers and 
maintainers of each.</span>
</font>
</p>
<p>
<font face="Verdana">
<span>Policies should be developed for Service Portfolio and 
for Service Catalog: recorded details, statuses, and 
responsibilities.</span>
</font>
</p>
<p>
<font face="Verdana">
<span>Service Catalog Aspects:</span>
</font>
</p>
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  font-weight: bold;  ">
<font face="Verdana">
<span>
<b>Business Service Catalog</b> ? A customer view: 
IT services delivered to the customer, including 
relations to business units and business 
processes it supports.</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  font-weight: bold;  ">
<font face="Verdana">
<span>
<b>Technical Service Catalog</b> ? IT Services 
related to customer ? related with 
infrastructure: supporting and shared services 
and CIs that support the provision of service. 
This view should not be presented to a 
customer.</span>
</font>
</li>
</ul>
</ul>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>4.1.5 Activities, methods and techniques</span>
</font>
</p>
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Agreeing and documenting a service definition 
with all relevant parties.</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interfacing with Service Portfolio Management 
to agree the contents of SP and SC.</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Production and maintainance of SC in 
conjunction with SP.</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interface with business and ITSCM on the 
relations of business units and processes with 
IT Services, therefore with contents of the 
Business Service Catalogue</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interface with support, suppliers and Config 
Management on relations contained in Technical 
Service Catalogue.</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Interface with Business Relationship 
Management and SLM to ensure that info 
(which info?) is aligned with the business and b. 
processes</span>
</font>
</li>
</ul>
</ul>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>4.1.6 Trigers, inputs, outputs and interfaces</span>
</font>
</p>
<p>
<font face="Verdana">
<span>Inputs:</span>
</font>
</p>
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Busines and IT strategy, plans, financial plans, 
current and future requirements from Service 
Portfolio</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Business Impact Analysis (impact, priority and 
risk related to each service or changes to svc. 
requirements)</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Business requirements: info on agreed, new or 
changed business reqs from Service Portfolio</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Service Portfolio</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>CMS</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Feedback from all other processes</span>
</font>
</li>
</ul>
</ul>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>Triggers are changes in business reqs &amp; services, 
consequently RFCs and ChM process. In the end, new 
services will be introduced, existing will be changed or 
retired.</span>
</font>
</p>
<p>
<br/>
</p>
<p>
<font face="Verdana">
<span>Outputs:</span>
</font>
</p>
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<ul style="margin-top: 0pt; margin-bottom: 0pt">
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Definition of the service documentation &amp; 
agreement</span>
</font>
</li>
<li style="margin-left: -3mm; margin-right: 0mm; padding-left: 0mm; font-family: Verdana;  ">
<font face="Verdana">
<span>Updated data on services and service 
